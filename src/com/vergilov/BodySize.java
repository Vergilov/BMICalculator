package com.vergilov;

public class BodySize {
    private int height;
    private int weight;
    private int waist;

    public BodySize(int height, int weight, int waist) {
        this.height = height;
        this.weight = weight;
        this.waist = waist;
    }

    public int getHeight() {
        return height;
    }

    public int getWeight() {
        return weight;
    }

    public int getWaist() {
        return waist;
    }
}
